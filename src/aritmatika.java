package aritmatika;

public class aritmatika {
    public static void main(String[] args) {
        int angka1 = 10;
        int angka2 = 20;

        int hasilTambah = angka1 + angka2;
        System.out.println("Hasil Penjumlahan"+ hasilTambah);

        int hasilkurang = angka1 - angka2;
        System.out.println("Hasil Penjumlahan"+ hasilkurang);

        int hasilbagi = angka1 % angka2;
        System.out.println("Hasil Penjumlahan"+ hasilbagi);

        int hasilkali = angka1 * angka2;
        System.out.println("Hasil Penjumlahan"+ hasilkali);
    }


}
